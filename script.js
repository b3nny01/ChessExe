let showSolution = false;

function checkSolution() {
	let exercise = $(".exercise-form>input[name='exercise']").val().toLowerCase().trim();
	let solution = $(".exercise-form>input[name='user-solution']").val().toLowerCase().trim();

	if (exercises.hasOwnProperty(exercise)) {
		if (exercises[exercise] === solution.toLowerCase().trim()) {

			$(".output>.wrong").css("visibility", "hidden");
			$(".output>.right").css("visibility", "visible");
		} else {
			$(".output>.right").css("visibility", "hidden");
			$(".output>.wrong").css("visibility", "visible");
		}
	} else {
		$(".output>.right").css("visibility", "hidden");
		$(".output>.wrong").css("visibility", "visible");
	}
}

function toggleSolution() {
	showSolution=!showSolution;
	if(showSolution){
		let exercise = $(".exercise-form>input[name='exercise']").val();
		$(".exercise-form>input[name='exercise-solution']").val(exercises[exercise])
		$(".exercise-form>.controls>input[name='toggle-solution']").val("hide solution")
	}else{
		$(".exercise-form>input[name='exercise-solution']").val("")
		$(".exercise-form>.controls>input[name='toggle-solution']").val("show solution")
	}
	
}
function onTextChange() {
	$(".output>img").css("visibility", "hidden");
}

function onLoad() {
	$(".exercise-form>input[type='text']").on("change", onTextChange);
	$(".exercise-form>.controls>input[name='check-solution']").on("click", checkSolution);
	$(".exercise-form>.controls>input[name='toggle-solution']").on("click", toggleSolution);
}